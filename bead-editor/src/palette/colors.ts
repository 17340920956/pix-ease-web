export function getColors(brand: string): BeadColor[] {
  return brands[brand]?.colors ?? artkalColors;
}

export function getColorHexMap(brand: string): Map<string, BeadColor> {
  const map = new Map<string, BeadColor>();
  const colors = getColors(brand);
  for (const c of colors) {
    map.set(c.hex.toUpperCase(), c);
  }
  return map;
}